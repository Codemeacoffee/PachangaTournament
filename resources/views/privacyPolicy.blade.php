                y es capaz de demostrarlo ante las autoridades de control competentes.
            </p>
            <p>
                En tal sentido, el responsable del tratamiento se regirá por los siguientes principios que deben servir a todo su personal como guía y marco de
                referencia en el tratamiento de datos personales:
            </p>
            <ol>
                <li>
                    <p>
                        <u>Protección de datos por defecto:</u> el responsable del tratamiento aplicará las medidas técnicas y organizativas apropiadas con miras
                        a garantizar que, por defecto, solo sean objeto de tratamiento los datos personales que sean necesarios para cada uno de los fines específicos
                        del tratamiento.
                    </p>
                </li>
                <li>
                    <p>
                        <u>Protección de datos por defecto:</u> el responsable del tratamiento aplicará las medidas técnicas y organizativas apropiadas con miras
                        a garantizar que, por defecto, solo sean objeto de tratamiento los datos personales que sean necesarios para cada uno de los fines específicos
                        del tratamiento.
                    </p>
                </li>
                <li>
                    <p>
                        <u>Protección de datos en el ciclo de vida de la información:</u> las medidas que garanticen la protección de los datos personales serán
                        aplicables durante el ciclo completo de la vida de la información.
                    </p>
                </li>
                <li>
                    <p>
                        <u>Licitud, lealtad y transparencia:</u> los datos personales serán tratados de manera lícita, leal y transparente en relación con el interesado.
                    </p>
                </li>
                <li>
                    <p>
                        <u>Limitación de la finalidad:</u> los datos personales serán recogidos con fines determinados, explícitos y legítimos, y no serán tratados
                        ulteriormente de manera incompatible con dichos fines.
                    </p>
                </li>
                <li>
                    <p>
                        <u>Minimización de datos:</u> los datos personales serán adecuados, pertinentes y limitados a lo necesario en relación con los fines para los que
                        son tratados.
                    </p>
                </li>
                <li>
                    <p>
                        <u>Exactitud:</u> los datos personales serán exactos y, si fuera necesario, actualizados; se adoptarán todas las medidas razonables para que se
                        supriman o rectifiquen sin dilación los datos personales que sean inexactos con respecto a los fines para los que se tratan.
                    </p>
                </li>
                <li>
                    <p>
                        <u>Limitación del plazo de conservación:</u> los datos personales serán mantenidos de forma que se permita la identificación de los interesados
                        durante no más tiempo del necesario para los fines del tratamiento de los datos personales.
                    </p>
                </li>
                <li>
                    <p>
                        <u>Integridad y confidencialidad:</u> los datos personales serán tratados de tal manera que se garantice una seguridad adecuada de los datos
                        personales, incluida la protección contra el tratamiento no autorizado o ilícito y contra su pérdida, destrucción o daño accidental, mediante
                        la aplicación de medidas técnicas u organizativas apropiadas.
                    </p>
                </li>
                <li>
                    <p>
                        <u>Información y formación:</u> una de las claves para garantizar la protección de los datos personales es la formación e información que
